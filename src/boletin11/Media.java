public class Media {
    
private String nom;
private String autor;
private int durSeg;

    public String getNom() {
        return nom;
    }

    public void setNom(String n) {
        this.nom = n;
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String a) {
        this.autor = a;
    }

    public int getDurSeg() {
        return durSeg;
    }

    public void setDurSeg(int d) {
        this.durSeg = d;
    }



    
}
